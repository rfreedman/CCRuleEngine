package com.carecaminnovations.mobile.model;

public class Updates {

	private String patientSummary;
	private String ruleset;
	private String messageSet;
	private String tipSet;
	private String vLearning;
	private String community;
	private String forms;
	private String activities;
	public String getPatientSummary() {
		return patientSummary;
	}
	public void setPatientSummary(String patientSummary) {
		this.patientSummary = patientSummary;
	}
	public String getRuleset() {
		return ruleset;
	}
	public void setRuleset(String ruleset) {
		this.ruleset = ruleset;
	}
	public String getMessageSet() {
		return messageSet;
	}
	public void setMessageSet(String messageSet) {
		this.messageSet = messageSet;
	}
	public String getTipSet() {
		return tipSet;
	}
	public void setTipSet(String tipSet) {
		this.tipSet = tipSet;
	}
	public String getvLearning() {
		return vLearning;
	}
	public void setvLearning(String vLearning) {
		this.vLearning = vLearning;
	}
	public String getCommunity() {
		return community;
	}
	public void setCommunity(String community) {
		this.community = community;
	}
	public String getForms() {
		return forms;
	}
	public void setForms(String forms) {
		this.forms = forms;
	}
	public String getActivities() {
		return activities;
	}
	public void setActivities(String activities) {
		this.activities = activities;
	}
	
	
}
