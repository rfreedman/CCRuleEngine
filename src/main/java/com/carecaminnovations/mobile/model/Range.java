package com.carecaminnovations.mobile.model;

public class Range{
	private String range;
	private float hi;
	private float low;
	public String getRange() {
		return range;
	}
	public void setRange(String range) {
		this.range = range;
	}
	public float getHi() {
		return hi;
	}
	public void setHi(float hi) {
		this.hi = hi;
	}
	public float getLow() {
		return low;
	}
	public void setLow(float low) {
		this.low = low;
	}
	
	
	
}